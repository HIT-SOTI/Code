DATAS SEGMENT
BUF1 DB 20H
     DB  0
     DB 20H DUP(0)
NUM  DW  ?
DATAS ENDS
   
STACKS SEGMENT PARA STACK
   DW 30H DUP(0)
STACKS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,SS:STACKS
START:
    MOV AX,DATAS
    MOV DS,AX
   
  	MOV AH,0AH
    LEA DX,BUF1
    INT 21H
    CALL STOI
    
    MOV AX,0
    MOV AH,4CH
    INT 21H
STOI PROC 
	MOV DX,0
	MOV BX,10
	MOV SI,2
	MOV NUM,0
	MOV AX,0
LOP:
    MOV AL,BUF1[SI]
    CMP AL,0DH
    JE  FINAL
    SUB AL,30H
    CMP NUM,0
    JE  DO_DEAL 
    PUSH AX
    MOV AX,NUM
    MUL BX 
    MOV  NUM,AX  
    POP AX
DO_DEAL:
    ADD NUM,AX
    MOV AX,0
    INC SI
    JMP LOP
FINAL:    
RET
STOI ENDP
CODES ENDS
    END START